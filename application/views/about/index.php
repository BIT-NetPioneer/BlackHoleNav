    <h2>BlackHole导航</h2>
    <p>
        BlackHole导航(BH-NAV)是网络开拓者协会于2012年暑假开启的项目——新校内导航站的子项目。BH-NAV彻底抛弃旧导航的架构和界面，只保留文字信息，从零开始设计搭建。
    </p>
    <h2>BlackHole历程</h2>
    <ul>
        <li>2012年8月 BH-NAV首个版本完成测试</li>
        <li>2012年7月 BH-NAV开始开发</li>
    </ul>
    <h2>名字由来</h2>
    <p>
        BlackHole即黑洞，其为恒星(star)的终结。黑洞本身即为一个密度高到难以想象的物质，正如同现在的网络一样，信息密度大得惊人，抓住每个靠近它的人。
    </p>
